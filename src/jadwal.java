    public boolean checkNO(String no)
    {
        PreparedStatement ps;
        ResultSet rs;
        boolean checkNO = false;
        String query = "SELECT * FROM `tbjadwal` WHERE`no` =?";
        
        try {
            ps = ConnectionMysql.getConnection().prepareStatement(query);
            ps.setString(1, no);
            
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                checkNO = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormRegitrasi.class.getName()).log(Level.SEVERE, null, ex);
        }
        return checkNO;
    }
 
