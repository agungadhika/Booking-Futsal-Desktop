    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String id = ID.getText();
        String nama = NAMA.getText();
        String nohp = NOHP.getText();
        String tugas = TUGAS.getText();
        String alamat = ALAMAT.getText();

        if(nama.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Mohon melengkapi data!");
        }

        else if(nohp.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Mohon melengkapi data!");
        }

        else if(tugas.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Mohon melengkapi data!");
        }
        else if(id.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Mohon melengkapi data!");
        }
        else if(alamat.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Mohon melengkapi data!");
        }
        else if(checkID(id))
        {
            JOptionPane.showMessageDialog(null, "Terdapat DATA yang sama! Mohon dicek Kembali!");
        }
        else if(checkNAMA(nama))
        {
            JOptionPane.showMessageDialog(null, "Terdapat DATA yang sama! Mohon dicek Kembali!");
        }

        else{
            PreparedStatement ps;
            String query = "INSERT INTO `tbkaryawan`(`NAMA`, `NO_HP`, `Tugas`,`Alamat`,`ID`) VALUES (?,?,?,?,?)";

            try {
                ps =ConnectionMysql.getConnection().prepareStatement(query);

                ps.setString(1, nama);
                ps.setString(2, nohp);
                ps.setString(3, tugas);
                ps.setString(4, alamat);
                ps.setString(5, id);

                if(ps.executeUpdate() > 0)
                {
                    JOptionPane.showMessageDialog(null, "Berhasil Registrasi,Mohon direfresh");
                }

            } catch (SQLException ex) {
                Logger.getLogger(FormRegitrasi.class.getName()).log(Level.SEVERE, null, ex);
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

