    public void getData( ){
 
        model.getDataVector().removeAllElements();
        //Memberitahu bahwa data kosong
        model.fireTableDataChanged();
        
        try {
            Connection c=DriverManager.getConnection("jdbc:mysql://localhost/dblogin","root","");
            Statement st=c.createStatement();
            //Perintah QUERY :
            String sql = "SELECT * FROM tbkaryawan";
            //Menjalankan perintah Query :
            ResultSet rs=st.executeQuery(sql);
            
            while (rs.next()) {
                Object[] o=new Object[5];
                o[0]=rs.getInt("ID");
                o[1]=rs.getString("Nama");
                o[2]=rs.getInt("NO_HP");
                o[3]=rs.getString("Tugas");
                o[4]=rs.getString("Alamat");
                model.addRow(o);
            }
            rs.close();
            st.close();
            
        } catch (SQLException e) {
            System.out.println("Terjadi Error dalam pengambilan data"+e);
        }
        
    }
